<!-- Exercício 16
Insira o valor 5 em uma variável, e o valor 3 em outra;
Teste os operadores de: igualdade, diferença, idêntico e não idêntico; -->

<?php
$a= 5;
$b= 3;
if ($a==$b){
  echo " a é igual a b ". "\n";
}
if ($a!=$b){
  echo " a é diferente a b " . "\n";
}
if ($a===$b){
  echo " a é idêntico a b ". "\n";
}
if ($a!==$b){
  echo " a não é a b ". "\n";
}